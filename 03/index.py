"""
16: Indexing and slicing with strings
"""

MY_STRING = "Hello World"
print(MY_STRING)
print(MY_STRING[0])
print(MY_STRING[8])
print(MY_STRING[9])
print(MY_STRING[-3])
print(MY_STRING[-1])

MY_STRING = "abcdefghijk"
print(MY_STRING)
print(MY_STRING[2:])
print(MY_STRING[:3])
print(MY_STRING[3:6])
print(MY_STRING[1:3])
print(MY_STRING[::2])
print(MY_STRING[2:7:2])
print(MY_STRING[::-1])
