"""
139. Advanced Strings
"""
STR = "hello\tworld"
print(STR.capitalize())
print(STR.lower())
print(STR.upper())
print(STR.count("o"))
print(STR.find("o"))
print(STR.center(20, "X"))
print(STR.expandtabs())
print(STR.isalnum())
print(STR.isalpha())
print(STR.islower())
print(STR.isupper())
print(STR.istitle())
print(STR.endswith("d"))
print(STR.split("o"))
print(STR.partition("o"))
